const fs = require('fs')
let data = fs.readFileSync('./jieba-dict.csv', 'utf8')
data = data.trim().split('\n').map((line) => {
  let parts = line.split(' ')
  let word = parts[0]
  let weight = parts[1]
  let pos = parts[2]
  pos = jieba2novelMapping[pos]
  return [word,pos,weight].join('|')
})
